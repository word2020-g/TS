void uart_timeout_handle(void * p_context)
{
    if(buffer.rxd_pos>1031)
        buffer.rxd_pos = 0;
    
    if(nrf_queue_push(&uart_rxd_queue,&buf)==NRF_SUCCESS)
    {
        buf.len = 0;
        buf.ptr = &buffer.rxd_buf[buffer.rxd_pos];
    }
    
    if(nrf_queue_is_full(&uart_rxd_queue))
        uart_rx_mutex = false;
}

/**@brief   Function for handling app_uart events.
 *
 * @details This function will receive a single character from the app_uart module and append it to
 *          a string. The string will be be sent over BLE when the last character received was a
 *          'new line' '\n' (hex 0x0A) or if the string has reached the maximum data length.
 */
/**@snippet [Handling the data received over UART] */
void uart_event_handle(app_uart_evt_t * p_event)
{
    switch (p_event->evt_type)
    {
        case APP_UART_DATA:
            if(uart_rx_mutex)
            {
                app_uart_get(&buffer.rxd_buf[buffer.rxd_pos]);
                buffer.rxd_pos += 1;
            }
            break;

        case APP_UART_COMMUNICATION_ERROR:
            APP_ERROR_HANDLER(p_event->data.error_communication);
            break;

        case APP_UART_FIFO_ERROR:
            APP_ERROR_HANDLER(p_event->data.error_code);
            break;

        default:
            break;
    }
}
